from enum import Enum


class AWSEndpointConnectionStatus(str, Enum):
    ENDPOINT_PENDING = "ENDPOINT_PENDING"
    ENDPOINT_PENDING_ACCEPTANCE = "ENDPOINT_PENDING_ACCEPTANCE"
    ENDPOINT_AVAILABLE = "ENDPOINT_AVAILABLE"
    ENDPOINT_DELETING = "ENDPOINT_DELETING"
    ENDPOINT_DELETED = "ENDPOINT_DELETED"
    ENDPOINT_REJECTED = "ENDPOINT_REJECTED"
    ENDPOINT_FAILED = "ENDPOINT_FAILED"
    ENDPOINT_EXPIRED = "ENDPOINT_EXPIRED"

    def __str__(self) -> str:
        return str(self.value)
