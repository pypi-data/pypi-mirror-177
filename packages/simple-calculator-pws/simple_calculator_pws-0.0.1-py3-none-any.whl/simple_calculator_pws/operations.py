# Submodulo de operacoes aritmeticas 

def soma (valor1, valor2):
    resultado = valor1 + valor2
    return f'A soma de {valor1:.2f} + {valor2:.2f} é : {resultado}'

def subtracao(valor1, valor2):
    resultado = valor1 - valor2
    return f'A subtração de {valor1:.2f} + {valor2:.2f} é : {resultado}'

def multiplicacao(valor1, valor2):
    resultado = valor1 + valor2
    return f'A Multiplicação de {valor1:.2f} * {valor2:.2f} é : {resultado}'

def divisao(valor1, valor2):
    resultado = valor1 + valor2
    return f'A divisão de {valor1:.2f} / {valor2:.2f} é : {resultado}'